test_that("check.metric works", {
    # expect_error(check.metric(1))
    expect_equal(check.metric(sum), "summary.metric")
    expect_equal(check.metric(var), "class.metric")
})

test_that("Output is correct", {
    expect_error(
        make.metric("a")
        )
    expect_error(
        make.metric(1)
        )
    expect_error(
        make.metric(function(x)as.character(x))
        )

    fun <- function(x, arg) {
        if(arg == TRUE){
            return(mean(x))
        } else {
            return(mean(x))
        }
    }

    expect_error(
        make.metric(fun, silent = FALSE)
        )

    fun2 <- function(x) {
        return("try-error")
    }
    expect_error(
        make.metric(fun2, silent = FALSE)
        )


    ## Verbose
    test <- function(x) as.character(x)
    error <- capture_error(make.metric(test, verbose = TRUE))
    expect_equal(error[[1]],
        "The provided metric function generated an error or a warning!\nDoes the following work?\n    test(matrix(rnorm(20), 5,4))\nThe problem may also come from the optional arguments (...) in test."
        )

    error <- capture_error(make.metric(test, silent = FALSE))
    expect_equal(error[[1]],
        "The provided metric function generated an error or a warning!\nDoes the following work?\n    test(matrix(rnorm(20), 5,4))\nThe problem may also come from the optional arguments (...) in test."
        )

    expect_error(
        make.metric(lapply)
        )

    expect_equal(
        make.metric(mean, silent=TRUE), "level1"
        )
    expect_equal(
        make.metric(ranges, silent=TRUE), "level2"
        )
    expect_equal(
        make.metric(var, silent=TRUE), "level3"
        )
    expect_equal(
        make.metric(function(x)mean(var(x)), silent=TRUE), "level1"
        )
    expect_equal(
        make.metric(function(x)variances(var(x)), silent=TRUE), "level2"
        )
    expect_equal(
        make.metric(function(x)var(var(x)), silent=TRUE), "level3"
        )
    expect_equal(
        make.metric(function(x)sd(variances(var(x))), silent=TRUE), "level1"
        )

    ## Same with data.dim
    expect_equal(
        make.metric(function(x)variances(var(x)), silent=TRUE, data.dim = c(3, 2)), "level2"
        )
    expect_equal(
        make.metric(function(x)var(var(x)), silent=TRUE, data.dim = c(33, 5)), "level3"
        )
    expect_equal(
        make.metric(function(x)sd(variances(var(x))), silent=TRUE, data.dim = c(3, 10)), "level1"
        )

    expect_equal(capture.output(make.metric(var)),
        c("var outputs a matrix object.", "var is detected as being a dimension-level 3 function.", "Additional dimension-level 2 and/or 1 function(s) will be needed."))

    expect_equal(capture.output(make.metric(sd)), 
        c("sd outputs a single value.", "sd is detected as being a dimension-level 1 function."))

    expect_equal(capture.output(make.metric(variances)), 
        c("variances outputs a matrix object.", "variances is detected as being a dimension-level 2 function."))

    error <- capture_error(make.metric(make.metric))
    expect_equal(error[[1]], "The provided metric function generated an error or a warning!\nDoes the following work?\n    make.metric(matrix(rnorm(20), 5,4))\nThe problem may also come from the optional arguments (...) in make.metric.")

    ## With between.groups
    between.groups.metric <- function(matrix, matrix2) return(42)
    between.groups.metric2 <- function(matrix, matrix2, option = TRUE) return(c(1,2,3,4))

    expect_equal(make.metric(between.groups.metric, silent = TRUE), "level1")
    expect_equal(make.metric(between.groups.metric, silent = TRUE, check.between.groups = TRUE), list("type" = "level1", "between.groups" = TRUE))
    expect_equal(make.metric(between.groups.metric2, option = FALSE, silent = TRUE), "level2")
    expect_equal(make.metric(between.groups.metric2, option = "bla", silent = TRUE, check.between.groups = TRUE), list("type" = "level2", "between.groups" = TRUE))
    expect_equal(make.metric(mean, silent = TRUE, check.between.groups = TRUE), list("type" = "level1", "between.groups" = FALSE))
})